package Day02;

public class Ex02_PlusMinusOperator {
	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		int c;
		
		c = ++a + b;
		System.out.println("c = " + c);
		System.out.println("a = " + a);
		
		int x = 10, y =20;
		int z;
		
		z = x++ + y;
		System.out.println("z = " + z);
		System.out.println("x = " + x);
		
		
	}
}
