package Day01;

public class EX09_Long {
	public static void main(String[] args) {
		// int : 2^32개(42억개) 
		// -21억.... ~ 21억....
		
		long ln1 = 10000;
		long ln2 = 2100000000;
		long ln3 = 2200000000L;
		
		System.out.println("ln1 : " + ln1);
		System.out.println("ln2 : " + ln2);
		System.out.println("ln3 : " + ln3);
		
	}
}
