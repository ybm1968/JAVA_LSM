package Day01;

public class EX08_Int {
	public static void main(String[] args) {
		// 리터럴 : 프로그램에서 직접 표현한 값
		// 정수
		// 10진수 
		// 8진수		: 리터럴 - 0숫자
		// 16진수 	: 리터럴 - 0x숫자
		
		int var1 = 10;		// 10진수
		int var2 = 010;		// 8진수
		int var3 = 0x10;	// 16진수
		
		System.out.println("var1 : " + var1);
		System.out.println("var2 : " + var2);
		System.out.println("var3 : " + var3);
	}

}
